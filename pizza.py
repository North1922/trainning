# 2) Пиццерия
#
# Сущности: Pizza (база, абстрактная), Margherita, Pepperoni, Order, Topping.
#
# Атрибуты:
#
# Pizza: name, базовая цена; список топпингов.
#
# Topping: name, extra_price.
#
# Order: список позиций (пицца, количество), статус.
#
# Методы (обязательные):
#
# Pizza.price() — посчитать цену с топпингами.
#
# Pizza.add_topping(topping) / remove_topping(topping) — с валидацией (нельзя дублировать, цена ≥ 0).
#
# Order.add(pizza, qty) / remove(pizza, qty) — qty > 0.
#
# Order.total() — итоговая стоимость.
#
# Order.checkout() — финализация заказа.
#
# Проверки/ошибки: отрицательные цены/кол-ва; пустой заказ к checkout().
#
# Инварианты: итог неотрицательный; после checkout() заказ становится read-only.
#
# Усложнения: скидки (стратегии), ограничения по несовместимым топпингам.